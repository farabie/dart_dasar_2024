void main() {
  //int adalah type data bilangan bulat
  int number1 = 20;
  //double adalah type data bilangan desimal
  double number2 = 20.5;

  print(number1);
  print(number2);

  //Jika ingin memuat int dan bilangan desimal kita bisa menggunakan num
  //Yang dimana num dapat memuat int dan double
  num numberBanyak = 100;
  print(numberBanyak);
}

