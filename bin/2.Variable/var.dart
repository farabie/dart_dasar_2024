void main() {
  //Kita bisa langsung menuliskan variable tanpa harus menyebutkan type datanya
  //Deklarasi Tidak Langsung
  var barang;

  barang = "Gelas";
  print(barang);

  //Rekomendasi deklarasinya secara langsung
  var sepatu = "Nike";
  print(sepatu);
  //Var ini juga bisa dideklarasikan ulang misal
  sepatu = "Adidas";
  print(sepatu);
}
