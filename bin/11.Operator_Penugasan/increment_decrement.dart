void main() {
  //Increment
  var angka1 = 10;
  var angka2 = 10;

  print(angka1);
  angka1++; //angka1 = angka1 + 1 (angka1)
  print(angka1);
  ++angka2; // angka2 = angka2 + 1 (angka2 + 1);
  print(angka2);

  //Decrement

  var x = 17;
  print(x);
  print(--x); //x = x - 1(x -1)
  print(x--); // x = x -1 (x)
}
