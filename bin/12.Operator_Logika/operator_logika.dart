void main() {
  var benar = true;
  var salah = false;

  //Contoh Operator &&
  var gabungan = benar && salah;
  print(gabungan);

  //Contoh Operator ||
  var a = true;
  var b = false;

  var ab = a || b;
  print(ab);

  //Contoh Operator !(Negasi)

  var x = true;
  print(!x);
  var y = false;
  print(!y);
}
